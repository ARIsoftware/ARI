#!/usr/bin/env node
/**
 * Generate lib/db/setup-sql.ts from lib/db/setup.sql.
 *
 * The bootstrap route (`app/api/auth/bootstrap/route.ts`) imports `setupSql`
 * as a string at build time so it can run the migration on a fresh `/welcome`
 * install. Hand-maintaining a TS mirror of the .sql file caused drift (see
 * the missing `ari_instance` table that broke the telemetry toggle on clean
 * installs). This script makes setup.sql the single source of truth.
 *
 * Idempotent: skips writing if the output is byte-identical.
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SQL_PATH = path.join(ROOT, 'lib', 'db', 'setup.sql');
const TS_PATH = path.join(ROOT, 'lib', 'db', 'setup-sql.ts');

function generateSetupSql() {
  let sql;
  try {
    sql = fs.readFileSync(SQL_PATH, 'utf-8');
  } catch (err) {
    console.error(`❌ Failed to read ${SQL_PATH}:`, err.message);
    process.exit(1);
  }

  // Escape sequences that would break a JS template literal.
  const escaped = sql
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');

  const output =
    `// AUTO-GENERATED by scripts/generate-setup-sql.js — DO NOT EDIT.\n` +
    `// Source of truth: lib/db/setup.sql\n` +
    `// Run \`npm run generate-module-registry\` to regenerate.\n` +
    `export const setupSql = \`${escaped}\`\n`;

  let existing = null;
  try {
    existing = fs.readFileSync(TS_PATH, 'utf-8');
  } catch {
    // File doesn't exist yet — fall through and write.
  }

  if (existing === output) {
    console.log('✅ setup-sql.ts already up-to-date');
    return;
  }

  fs.writeFileSync(TS_PATH, output, 'utf-8');
  console.log(`✅ setup-sql.ts regenerated from setup.sql (${sql.length} chars)`);
}

if (require.main === module) {
  generateSetupSql();
}

module.exports = generateSetupSql;
